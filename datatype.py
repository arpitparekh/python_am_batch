# tuple
# immutable # ordered # fixed size

cars = ("Ford", "Volvo", "BMW","BMW")
print(cars)
print(type(cars))

# set
# mutable # unordered # no duplicate

vegetables = {"tomato", "potato", "onion", "carrot","onion","onion","ladyfinger","binjal"}
print(vegetables)
